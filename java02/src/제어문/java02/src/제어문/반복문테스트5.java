package 제어문;

public class 반복문테스트5 {

	public static void main(String[] args) {
		
		for (int i = 0; i < 10; i++) {
			//0부터 시작해서 i < 10 하면 0~9까지 10개나옴
			System.out.println(i + ": 반갑습니다.");
			
		}
		
		
		for(int count = 10; count >= 1; count--) {
			System.out.println(count + ": 안녕히가세요");
		}
		
	}

}
